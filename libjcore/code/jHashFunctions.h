/******************************************************************************
 JDualHash

 *****************************************************************************/

inline JDualHashValue
JDualHash
	(
	const JHashValue hashValue
	)
{
	// Generate new word-sized value
	JHashValue newHash = JRandWord(hashValue);

	// Convert to a dual hash-value; see notes
	return ( ( (newHash >> 9) & 0xFF ) | 1L) - 0x80;
}

